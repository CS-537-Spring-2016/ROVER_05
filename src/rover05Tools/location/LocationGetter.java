package rover05Tools.location;

public class LocationGetter {
	
	int x;
	int y;
	
	
	public LocationGetter(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}
	
	
	public LocationGetter() {
	}



	public int getX() {
		return x;
	}


	public void setX(int x) {
		this.x = x;
	}


	public int getY() {
		return y;
	}


	public void setY(int y) {
		this.y = y;
	}
	
	

}
